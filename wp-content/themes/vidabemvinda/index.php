              <p>A Robótica trouxe avanços interessantes à cirurgia minimamente invasiva moderna, integrando a engenharia da computação com a destreza cirúrgica. É uma tecnologia que nos permite realizar movimentos extremamente precisos e delicados, associados a uma visão 3D e de alta resolução (full HD) do campo cirúrgico. O cirurgião opera sentado num console, e os “braços” robóticos são acoplados à paciente através de pequenos trocartes, por onde introduzimos pinças cirúrgicas. Quem opera a paciente é o médico, sendo que o robô apenas traduz os movimentos do cirurgião para as pinças. Os principais diferenciais da cirurgia robótica são a visão tridimensional em alta resolução e a flexibilidade de movimentos nas pontas dos instrumentos, o que não é possível com a laparoscopia. Para realizar cirurgias robóticas, é necessário um treinamento e certificação específica. O Dr. Renato Tomioka é licenciado pela Intuitive Surgical® (Da Vinci System) para realizar cirurgias robóticas no Brasil. Como a laparoscopia, a robótica nos permite realizar todas as cirurgias ginecológicas minimamente invasivas.</p>
