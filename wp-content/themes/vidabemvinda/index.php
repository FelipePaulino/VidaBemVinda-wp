              <h2>Histeroscopia</h2>
              <p>Histeroscopia ou Videohisteroscopia é um procedimento que nos permite avaliar e realizar operações por dentro do útero, por meio de um histeroscópio, que consiste em um sistema fino e delicado, que inclui uma cânula com menos de 5 mm, por onde passam a ótica e os instrumentos cirúrgicos. Por meio do colo uterino, o histeroscópio nos traz imagens intrauterinas em alta resolução, sendo o gold standard para detecção e tratamento de doenças como pólipos, miomas submucosos, sinéquias, endometrite, septos uterinos e outras malformações.
              <p>A Histeroscopia pode ser de dois tipos: a diagnóstica (quando já existe suspeita de alterações em outros exames, como no ultrassom) ou cirúrgica (quando já foi realizado um diagnóstico prévio). Em geral, a histeroscopia diagnóstica é feita sem anestesia em ambiente ambulatorial (consultório ou laboratórios), no entanto, pode ser realizada com sedação, em casos de dores. A histeroscopia cirúrgica é feita com anestesia, na maioria dos casos.
</p>
