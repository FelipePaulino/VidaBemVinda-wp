              <h2>Laparoscopia</h2>
              <p>Laparoscopia ou Videolaparoscopia é uma técnica de cirurgia minimamente invasiva em que utilizamos pequenas incisões na pele (3 a 12mm) para realizar diversos procedimentos. Em geral, usamos uma punção no umbigo, por onde entra a ótica (laparoscópio), e 2 ou 3 punções na região inferior do abdome, por onde passam instrumentos especializados. O sistema é acoplado a um insuflador de CO2, uma fonte de luz de xênon, câmera e tela full HD e até 4K (altíssima resolução), o que nos permite identificar estruturas e órgãos com mais detalhes do que a olho nu. As vantagens incluem: menos dor e recuperação pós-operatória mais rápida, menor risco de aderências intra-abdominais, cicatrizes menores e menor tempo de internação hospitalar.
